minha_lista = [3,13,21,27,33]
print(minha_lista[3]) # index

mais_de_uma = [ [1,2,3] , [4,5,6] , [7,8,9] ] 

print(mais_de_uma[0]) # 0 pega a primeira lista
print(mais_de_uma[1]) # 1 pega a segunda lista

# Para pegar dentro das listas, vamos criar dois []
print(mais_de_uma[0][2]) # Dentro da lista 0, index 3

print(mais_de_uma[0][1:]) # 1: ai vou excluir o 1
print(mais_de_uma[0][:1]) # :1 aí vou pegar só até o 1