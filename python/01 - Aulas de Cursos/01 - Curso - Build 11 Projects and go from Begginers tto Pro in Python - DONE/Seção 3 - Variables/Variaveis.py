## Variables são caixas que contem dados - cada uma tem seu nome e valor

number1 = 3 # variavel NUMBER com valor 3
number2 = 7

# Se eu escrever o nome da variavel, só vai aparecer o dado dela, usar o print()
print(number1)

# para achar qual o type da variable, vamos usar o type()
print(type(number1))

# Testando outro Type
name = "Raul"
print(name)
print(type(name))

total = (number1 + number2)
print(total)
